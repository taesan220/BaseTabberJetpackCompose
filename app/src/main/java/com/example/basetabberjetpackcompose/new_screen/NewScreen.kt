@Composable
fun NewScreen(onBack: () -> Unit) {
    Box(
        modifier = Modifier
            .fillMaxSize()
            .padding(16.dp),
        contentAlignment = Alignment.Center
    ) {
        Column(horizontalAlignment = Alignment.CenterHorizontally) {
            Text("새로운 화면")

            Spacer(modifier = Modifier.height(20.dp))

            Button(onClick = onBack) {
                Text("뒤로 가기")
            }
        }
